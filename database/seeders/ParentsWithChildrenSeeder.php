            if (!$class) {
                $this->command->error("Class not found for grade {$studentData['grade_level']} section {$studentData['section']}. Skipping student {$studentData['first_name']} {$studentData['last_name']}");
                $failedStudents++;
                continue;
            }

            // Check for duplicate LRN
            if (in_array($studentData['lrn'], $usedLrns)) {
                $this->command->error("Duplicate LRN {$studentData['lrn']} for student {$studentData['first_name']} {$studentData['last_name']}. Skipping.");
                $failedStudents++;
                continue;
            }
            $usedLrns[] = $studentData['lrn'];

            // Create student
            $student = Student::create([
