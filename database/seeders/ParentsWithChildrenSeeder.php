        foreach ($studentsData as $index => $studentData) {
            $parentNumber = $index + 1;
            
            // Validate if the grade level exists
            if (!array_key_exists($studentData['grade_level'], $gradeSections)) {
                $this->command->error("Invalid grade level '{$studentData['grade_level']}'. Available grade levels: " . implode(', ', array_keys($gradeSections)));
                $failedStudents++;
                continue;
            }

            // Validate if the section is valid for the grade level
            if (!in_array($studentData['section'], $gradeSections[$studentData['grade_level']])) {
                $validSections = implode(', ', $gradeSections[$studentData['grade_level']]);
                $this->command->error("Invalid section '{$studentData['section']}' for grade level '{$studentData['grade_level']}'. Valid sections: {$validSections}");
