                'middle_name' => $studentData['middle_name'],
                'last_name' => $studentData['last_name'],
                'class_id' => $class->id,
                'parent_id' => $parent->id,
                'approved_by_teacher' => true,
                'lrn' => $studentData['lrn'],
                'gender' => $studentData['gender'],
            ]);

            $this->command->info("✓ Created student: {$studentData['first_name']} {$studentData['last_name']} in Grade {$studentData['grade_level']} - {$studentData['section']}");
