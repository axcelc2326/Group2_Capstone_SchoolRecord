                continue;
            }

            // Generate unique email
            $lastNameClean = preg_replace('/[^a-zA-Z0-9]/', '', $studentData['last_name']);

            // Get only the first word of the first name (full word, not shortened)
            $firstNameParts = explode(' ', $studentData['first_name']);
            $firstWord = $firstNameParts[0];
            $firstNameClean = preg_replace('/[^a-zA-Z0-9]/', '', $firstWord);

            $baseEmail = strtolower("{$lastNameClean}_{$firstNameClean}@example.com");
            $email = $baseEmail;
            $counter = 1;

            while (User::where('email', $email)->exists()) {
                $email = strtolower("{$lastNameClean}_{$firstNameClean}{$counter}@example.com");
                $counter++;
            }

